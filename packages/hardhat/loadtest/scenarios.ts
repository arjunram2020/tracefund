export async function donateScenario(ctx: ScenarioContext) {
  if (ctx.world.campaigns.length === 0) return;
  const target = pick(ctx.world.campaigns, ctx.rng);
  if (target.creatorIndex === ctx.wallet.index) return; // creators can't donate to their own campaign

  const campaign = await ctx.covenant.getCampaign(target.id);
  if (!campaign.active) return;
  const room = campaign.goalAmount - campaign.totalRaised;
  if (room <= 0n) return;
  const failed = await ctx.covenant.milestoneFailed(target.id);
  if (failed) return;

  const cap = room < usdc6(20) ? room : usdc6(randomInt(ctx.rng, 1, 20));
  const amount = cap <= 0n ? room : cap;

  const allowance = await ctx.usdc.allowance(ctx.wallet.wallet.address, ctx.covenantAddress);
  if (allowance < amount) {
    const approveOutcome = await runWrite(
      () => ctx.usdc.connect(ctx.wallet.wallet).approve(ctx.covenantAddress, ethers.MaxUint256),
      ctx.maxRetries,
    );
    recordWrite(ctx, "approveUsdc", approveOutcome);
    if (!approveOutcome.ok) return;
  }

  const outcome = await runWrite(
    () => ctx.covenant.connect(ctx.wallet.wallet).donate(target.id, amount),
    ctx.maxRetries,
  );
  recordWrite(ctx, "donate", outcome);
}

export async function claimRefundScenario(ctx: ScenarioContext) {
  if (ctx.world.campaigns.length === 0) return;
  const target = pick(ctx.world.campaigns, ctx.rng);
  const refundable = await ctx.covenant.refundOf(target.id, ctx.wallet.wallet.address);
  if (refundable <= 0n) return;

  const outcome = await runWrite(
    () => ctx.covenant.connect(ctx.wallet.wallet).claimRefund(target.id),
    ctx.maxRetries,
  );
  recordWrite(ctx, "claimRefund", outcome);
}

