export async function reviewProofScenario(ctx: ScenarioContext) {
  if (ctx.world.campaigns.length === 0) return;
  // Sample a handful of campaigns rather than scanning all of them — this
  // wallet is only ever authorized on a small subset.
  const sample = Array.from({ length: Math.min(5, ctx.world.campaigns.length) }, () =>
    pick(ctx.world.campaigns, ctx.rng),
  );

  for (const target of sample) {
    const isReviewer = await ctx.covenant.isReviewer(target.id, ctx.wallet.wallet.address);
    if (!isReviewer) continue;

    const campaign = await ctx.covenant.getCampaign(target.id);
    if (!campaign.active || campaign.currentMilestone >= BigInt(campaign.milestoneCount)) continue;

    const milestone = await ctx.covenant.getMilestone(target.id, campaign.currentMilestone);
    if (Number(milestone.state) !== 1) continue; // 1 = Submitted

    const submissions = await ctx.covenant.getSubmissions(target.id, campaign.currentMilestone);
    const submissionIndex = submissions.length - 1;
    const already = await ctx.covenant.hasReviewed(
      target.id,
      campaign.currentMilestone,
      BigInt(submissionIndex),
      ctx.wallet.wallet.address,
    );
    if (already) continue;

    const approve = ctx.rng() < 0.85;
    const notes = approve ? "" : "Evidence insufficient for the stated acceptance criteria (synthetic rejection).";

    const outcome = await runWrite(
      () => ctx.covenant.connect(ctx.wallet.wallet).reviewProof(target.id, approve, notes),
      ctx.maxRetries,
    );
    recordWrite(ctx, "reviewProof", outcome);
    return; // one review action per turn is realistic
  }
}

// ---------------------------------------------------------------------------
// Read-only scenarios (all roles use these; read-only wallets use ONLY these)
// ---------------------------------------------------------------------------

export async function readScenario(ctx: ScenarioContext) {
  const reads: Array<() => Promise<unknown>> = [
    () => ctx.covenant.getAllCampaigns(),
    () => ctx.covenant.campaignCount(),
  ];

  if (ctx.world.campaigns.length > 0) {
    const target = pick(ctx.world.campaigns, ctx.rng);
    reads.push(
      () => ctx.covenant.getCampaign(target.id),
      () => ctx.covenant.getMilestones(target.id),
      () => ctx.covenant.getApprovalConfig(target.id),
      () => ctx.covenant.getDonors(target.id),
      () => ctx.covenant.refundOf(target.id, ctx.wallet.wallet.address),
      async () => {
        const campaign = await ctx.covenant.getCampaign(target.id);
        return ctx.covenant.trustScore(campaign.creator);
      },
    );
  }

  const read = pick(reads, ctx.rng);
  const outcome = await runRead(read);
  recordRead(ctx, "read", outcome);
}
